#include "framework.h"

Monster::Monster(string cName, int cAttack, int cHealth)
{
    name = cName;
    attack = cAttack;
    health = cHealth;
}

    Monster::~Monster()
    {
    }

	/*------------------------- Getters and setters -------------------------*/
// Name
	string Monster::getName()
	{
		return name;
	}
	void Monster::setName(string newName)
	{
		name = newName;
	}
	// Attack
	int Monster::getAttack()
	{
		return attack;
	}
	void Monster::setAttack(int newAttack)
	{
		attack = newAttack;
	}
	// Health
	int Monster::getHealth()
	{
		return health;
	}
	void Monster::setHealth(int newHealth)
	{
		health = newHealth;
	}
